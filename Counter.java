// counter program
public class Counter
{
   //declare instance variable here
   private int count;
   
   public int getValue()
   {
	//provide body here
   return count;

   }

   public void click() 
   {
	//provide body here
   count = count + 1;

   }

   public void reset()
   {
	//provide body here

   count = 0;
   }
}
